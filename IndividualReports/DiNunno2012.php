<h2>Report 2011-2012 by  Giulia Di Nunno  (April 2012)</h2>
 <p>  
&nbsp; &nbsp;
I have continued relationships with Sub-Saharan Africa in particular with
SAMSA (Southern African Mathematical Sciences Association) and I have
taken active part at the annual conference, which was held in Zambia,
Livingstone, at the end of November 2011.
In this occasion, we have had chance of networking, bringing some members
of ISP and the CDC to take part to the conference. Some other colleagues
from Germany and England have also joined showing interested in
supervising students at distance from the various local universities.

<br />
&nbsp; &nbsp;
Being member of the Norwegian Master Program "Mathematical modelling"
funded by NOMA and based in Dar-es-Salam, Tanzania, I have followed the
progression of the students and also had the steering committee meeting
during the very same SAMSA conference. The program is very successful in
terms of students finishing and in terms of gender ratio of participation.

<br />
&nbsp; &nbsp;
As new activity, I am now involved in CIMPA as Norway has agreed in
joining the network as partner. The partnership will be officially
formalized this June in Paris during the General Assembly, however the
whole year has been spotted by background work between the mathematician
involved, the Norwegian Mathematical Society and the Research Council. On
another level also the Norwegian Ministry of Research and Education was
strongly involved.
Within CIMPA, I'm joining Marie-Francoise Roy's discussion group about the
development of mathematics in Sub-Saharan Africa.

<br />
&nbsp; &nbsp;
In June 2011, a student of mine from Tunisia has completed her PhD studies.
