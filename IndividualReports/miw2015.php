<h2>Report of activities in 2014-2015 by Michel Waldschmidt (March 2015)
</h2>
 
<body>
<p>Between the meeting in Berlin in April 2014 and the meeting
in Oslo in April 2015, my main activities related with EMS-
CDC have been the following ones. I had a number of
visits in developing countries ­ see <a href = "http://www.math.jussieu.fr/~miw/AgendaArchives.html">my web site</a>;
some of them gave rise to a <a href = "http://www.math.jussieu.fr/~miw/cooperations.html">report</a>.
</p>
<ul>
<li>From 7 to 19 July 2014, I participated in the <a href = "http://www.cimpa-icpam.org/spip.php?article571">CIMPA-ICTP-BENIN Research School</a> <i>Algebraic Number Theory and Applications</i>, <a href = "http://www.imsp-uac.org/CIMPA_research_school/">The Institute of Mathematics and Physical
Sciences (IMSP)</a>, Dangbo (Benin). On July 21 I participated in a conference in <a href = "http://www.univ-lome.tg/">Université de Lomé</a>, Togo and on July 24 to a Journée Arithmétique at <a href = "http://www.univ-fhb.edu.ci/fr/index.php/">Université Félix Houphouët-Boigny</a>, Cocody, Abidjan,
Ivory Coast.
<li> On August 12, 2014, I participated in the symposium <a href = "http://webusers.imj-prg.fr/~michel.waldschmidt/EMS-CDC/EMS-CDC-Depliant2014.pdf">"Mathematics in Emerging Nations: Achievements and
Opportunities" (MENAO)</a>. There, I distributed a <a href = "http://webusers.imj-prg.fr/~michel.waldschmidt/EMS-CDC/EMS-CDC-Depliant2014.pdf">EMS­CDC leaflet</a> a <a href = "http://webusers.imj-prg.fr/~michel.waldschmidt/EMS-CDC/EMC-CDC2014beamer.pdf">EMS­CDC poster</a> and I gave a <a href = "http://webusers.imj-prg.fr/~michel.waldschmidt/EMS-CDC/EMC-CDC2014beamer.pdf">beamer presentation on EMS­CDC</a>. In Seoul there was a joint
meeting with EMS Committee for European Solidarity: see my <a href = "http://euro-math-soc.eu/EMS-CDC/MeetingSeoul15082014.pdf">report</a>.

