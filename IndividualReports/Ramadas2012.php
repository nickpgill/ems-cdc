&nbsp; &nbsp; &nbsp; &nbsp; 

  
Funding (from the Italian government) has been stable. I hope
organizations which are looking to encourage mathematicians from
developing countries will consider working in cooperation with ICTP.

<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
Fernando Rodriguez Villegas was recruited to lead number theory
initiatives at ICTP and help consolidate ties to South America. Originally
from Argentina, he has been for many years at the University of Texas at
Austin. With a strong research record and deep commitment to outreach, he
is sure to enrich the Section in many ways; he is expected to join us this
Fall.

<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
Professor Maria José Pacifico from the  Instituto de Matemática,
Universidade Federal do Rio de Janeiro was appointed Staff Associate and
is expected to work with Stephano Luzzatto to strengthen our activities in
dynamical systems.


<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
Many in the 2011 batch of Diploma students were placed in very good
European graduate programmes – one (from Senegal) in the Ecole
Polytechnique, two (Cameroon, Egypt) at the MPI, Leipzig, and two (Cuba,
Mongolia) in the new ICTP/SISSA joint Ph.D. programme.


<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
The Ramanujan Prize for 2011 was awarded to Professor Philibert Nang (Gabon).


<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
In a novel initiative, we organized a short meeting in Rabat where a group
of approximately 30 young women were introduced to themes from modern
Mathematics. The highlight was undoubtedly the presence of Professor
Michele Audin (Strasbourg) who – in addition to two talks with
Mathematical themes - also gave the assembled students advice on
Mathematics as a career choice for women. Three bourses were awarded.


<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
We had, as in every year, around 100 visitors from developing countries;
of whom around 10 were long-term (6 months to two years). We have now the
second of two post-docs from ASSMS, Lahore. Both have been women.


<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
Our Associates Programme continues to attract many applicants from around
the developing world.

<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
FYI:  Dr. Basang Tsering Xiao has been appointed an Associate this year.


