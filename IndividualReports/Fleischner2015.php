<h2>Herbert Fleischner (2015)
</h2>
<p>
Two years ago, I contributed a report on the situation of the maths de-
partment at UZ (University of Zimbabwe). In this report I made it clear

that one has to start more or less from scratch in developing mathematics

at UZ. The situation there has now improved to some extent. A Zim-
babwean graph theorist returned from South Africa and is organizing a

regional conference on Discrete Mathematics to be held at UZ July 6-10.

I’ll be there most likely, also to discuss the possibility of re-vitalizing the

MSc Programme in Discrete Mathematics (whose predecessor was a pilot

programme directed by myself in 1997-2000), aiming at academic staff de-
velopment. Once the picture of this programme is clearer, I will inform

EMS-CDC and discuss the role EMS-CDC might play in this context (not

so much of a financial role, but rather endorsing the programme, say). I

have also addressed Paul Vaderlind in a mail today, in which I envision

PACM acting like an ’umbrella’ for this programme. We might view this

anticipated programme as a first step to put UZ back on the mathematical

map.
</p><p>
There are several staff members in that department who are graduates of

the Norwegian NUFU programme on mathematical modelling, but they are

not so much trained as theoretical mathematicians. So, the programme we

intend to develop is supposed to be proof oriented. I’ll keep you informed

anyway.
</p> 

