<h2>Report by Joana Teles (April 2014)</h2>
<p>I continue being involved with the Portuguese speaking Mathematical Olympiad.  The invited countries for this competition are Portugal, Brazil, Angola, S&atilde;o Tom&eacute; and Principe, Guine-Bissau, Cape Verde, Mozambique and East Timor. 
</p><p>
The first edition was held in Coimbra, Portugal, in 2011, with 6 countries participating with students (S&atilde;o Tom&eacute; and Principe and Guine-Bissau participated with an observer). The second one was held in 2012, in Salvador da Bahia, Brazil with 7 countries with students (just missed Guine-Bissau). 
</p><p>
Last year, in August, Mozambique organized the third edition of this Olympiad. This year I will be in Angola in July at an international conference about teaching of Mathematics integrated in the fourth edition of this Olympiad. 
</p><p>
The University of Coimbra received last March, for two weeks, four teachers from ISPTEC in Luanda, Angola. These teachers came to seek help to improve performance in their classes, but there is the possibility that these teachers pursue studies for MSc or PhD in Mathematics in Portugal.
</p>

