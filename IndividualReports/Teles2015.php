<h2>Report by Joana Teles (April 2015)</h2>
 <p>I continue being involved with the Portuguese speaking Mathematical Olympiad. The invited countries for this competition are Portugal, Brazil, Angola, São Tomé and Principe, Guine-Bissau, Cape Verde, Mozambique and East Timor. The first edition was held in Coimbra, Portugal, in 2011, with 6 countries participating with students (São Tomé and Principe and Guine-Bissau participated with an observer). The second one was held in 2012, in Salvador da Bahia, Brazil with 7 countries with students (just missed Guine-Bissau) and the third one was held in Maputo, Mozambique with only 4 countries.
</p><p>Last year, in August, Angola organized the fourth edition of this Olympiad. I was there to help them in this event. This year I will be in Cape Verde in July in the fifth edition of this Olympiad.
</p><p>The University of Coimbra  began to receive students from Angola and is offering them a year of classes (called zero year) in order to prepare them to enter a graduation in mathematics.
