<h2>REPORT OF THE COMMITTEE FOR DEVELOPING COUNTRIES OF EMS</h2>
  Tsou Sheung Tsun, Michel Waldschmidt (14.11.11).
 <p><b>
SAD NEWS
</b>
<br />  &nbsp; &nbsp;
  It is with the greatest regret and sadness that we report the
untimely death of our member Mikael Passare in September.
<br />
  Mikael officially joined the Committee in 2008, but many of us knew him as
a treasured colleague for much much longer.  In spite of his very busy
schedule he always had time for CDC business.  He was our Treasurer,
and following our tradition but unlike treasurers of other commitees, was 
relaxed about what we spent, since he believed in our work. He
attended all our meetings, greatly contributed to the serious but
genial atmosphere.  As we all know, as well as being a great traveller,
he was also a great linguist and he used to speak French with our 
francophone colleagues, much to their content.

<br />  <b>
MEMBERSHIP
</b>
<br />  &nbsp; &nbsp;
  We are looking to replace Mikael and other members who will retire
at the end of the year.  A process has been started and when we come
up with we shall submit them for EC approval.

<br />  <b>
ISP ANNIVERSARY MEETING
</b>
<br />  &nbsp; &nbsp;
  Our member Giulia di Nunno represented our committee at the 50th
Anniversary of the International Science Programme (ISP) at Uppsala in
September.  It was a great meeting, unfortunately saddened by the
death of Mikael who was one of their Board Directors.

<br />  <b>
REDUCED FEES
</b>
<br />  &nbsp; &nbsp;
Under the great impetus of Mireille Martin-Deschamps (and also help of
President Marta Sanz-Sole and Treasurer Jouko Vaananen), the committee
established, after wide consultation, a list of developing countries
for which a reduced fee of 5 euros is suggested for individuals to
join EMS.  This list is now on the EMS website. 

<br />  <b>
GENDER IMBALANCE IN SUBSAHARAN AFRICA
</b>
<br />  &nbsp; &nbsp;
  CIMPA has found that there is a marked small percentage of women
mathematicians participating in its Research Schools, well under 10 %.
Jointly with CIMPA we supported the participation of Marie-Francoise     
Ouedraogo from Bukina Faso to the Eureopean Women in Mathematics in
September at Barcelona.  She is the chair of the committee on women
mathematicians of the African Union of Mathematics.  At the meeting
she was able to make known the sorry state of affairs in Subsaharan
Africa.  She later gave an interview in the EWM Newsletter, and we
hope that something positive will come out of this exposure.

<br />  <b>
ELECTRONIC ACCESS
</b>
<br />  &nbsp; &nbsp;
  The collaboration with IMU is continuing.  Lena Kock, from the Global
Secretariat of IMU, will coordinate with member Anders Wandhal to
apply for a grant from the Gates Foundation for their Access to
Learning Award 2012. 

