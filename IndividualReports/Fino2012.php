On March 2012 at the University of Lecce Anna Fino met Amirhesam Zaeim
who is a Phd student from Payame Noor University, Teheran, Iran.  He
attended the PhD Course that she was teaching in Lecce.  Anna Fino will
try to keep in contact with Amirhesam Zaeim and his university.

<br />
&nbsp; &nbsp; &nbsp; &nbsp; 
Anna Fino is also from the last year in contact with Amine Bahayou from
Universite Kasdi Merbah, Ouargla, Algeria. He just finished his PhD
and in his small university, together with other people, he created a
small working group around the themes of geometry and algebra. This 
is on some elementary topics: Differential Geometry (Lie groups and 
Lie algebras, connections and bundles), algebraic topology (homotopy 
and covering with the categorical language) ... for introduction 
and upgrading of teachers.   He also prepared a project of
Licence and Masters in geometry and algebra, for submission to the
Scientific Committee.  Anna Fino will try to give them advice for the 
selection of topics and teaching materials.
